    private void btnExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcelActionPerformed
        // TODO add your handling code here:
        String path=null,tenfile=null;
        boolean flag;
        do{
            flag=false;
            tenfile = JOptionPane.showInputDialog("Nhập tên file :");
            if(tenfile == null){
                return;
            }
            if(tenfile.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tên file");
                flag=true;
            }
            else if (tenfile.contains(" ")){
                JOptionPane.showMessageDialog(rootPane, "Tên file không có khoảng trắng");
                flag=true;
            }
            else if (tenfile.contains("/") || tenfile.contains("%") || tenfile.contains("#") || tenfile.contains(":") ||tenfile.contains(";") ||tenfile.contains("~") ||tenfile.contains(".") ){
                JOptionPane.showMessageDialog(rootPane, "Tên file không chứa kí tự đặc biệt");
                flag=true;
            }
        }
        while(flag);
        
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if(x == JFileChooser.APPROVE_OPTION){
            path=j.getSelectedFile().getPath()+"//"+tenfile;
        }
        FileOutputStream file;

        XSSFWorkbook wb = new XSSFWorkbook();
        XSSFSheet Sheet = wb.createSheet("KhoLaptop");
        XSSFRow row = null;
        Cell cell = null;
        row = Sheet.createRow(0);
        
        cell = row.createCell(0, CellType.STRING);
        cell.setCellValue("ID");
        cell = row.createCell(1, CellType.STRING);
        cell.setCellValue("Tên");
        cell = row.createCell(2, CellType.STRING);
        cell.setCellValue("Số lượng tồn kho");
        cell = row.createCell(3, CellType.STRING);
        cell.setCellValue("Giá");
        cell = row.createCell(4, CellType.STRING);
        cell.setCellValue("CPU");
        cell = row.createCell(5, CellType.STRING);
        cell.setCellValue("RAM");
        cell = row.createCell(6, CellType.STRING);
        cell.setCellValue("GPU");
        cell = row.createCell(7, CellType.STRING);
        cell.setCellValue("  ");
        cell = row.createCell(8, CellType.STRING);
        cell.setCellValue("ID riêng");
        cell = row.createCell(9, CellType.STRING);
        cell.setCellValue("Ngày lập");
        cell = row.createCell(10, CellType.STRING);
        cell.setCellValue("Mẫu laptop");
        
        for(int i=0;i<tblLaptop.getRowCount();i++) {
            row = Sheet.createRow(i+1);
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue(tblLaptop.getValueAt(i, 0).toString());
            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue(tblLaptop.getValueAt(i, 1).toString());
            cell = row.createCell(2, CellType.NUMERIC);
            cell.setCellValue((int) tblLaptop.getValueAt(i, 2));
            cell = row.createCell(3, CellType.NUMERIC);
            cell.setCellValue((int) tblLaptop.getValueAt(i, 3));
            cell = row.createCell(4, CellType.STRING);
            cell.setCellValue(tblLaptop.getValueAt(i, 4).toString());
            cell = row.createCell(5, CellType.NUMERIC);
            cell.setCellValue((int) tblLaptop.getValueAt(i, 5));
            cell = row.createCell(6, CellType.STRING);
            cell.setCellValue(tblLaptop.getValueAt(i, 6).toString());
            cell = row.createCell(7, CellType.STRING);
            cell.setCellValue(" ");
            cell = row.createCell(8, CellType.STRING);
            cell.setCellValue(tblCTLaptop.getValueAt(i, 0).toString());
            cell = row.createCell(9, CellType.STRING);
            cell.setCellValue(tblCTLaptop.getValueAt(i, 1).toString());
            cell = row.createCell(10, CellType.STRING);
            cell.setCellValue(tblCTLaptop.getValueAt(i, 2).toString());
        }
        try {
            file = new FileOutputStream(path+".xlsx");
            wb.write(file);
            wb.close();
            file.close();

        } catch (IOException ex) {
            Logger.getLogger(Laptop_DAO.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(path != null){
            JOptionPane.showMessageDialog(rootPane, "Xuất file Excel thành công");
        }
    }//GEN-LAST:event_btnExcelActionPerformed

