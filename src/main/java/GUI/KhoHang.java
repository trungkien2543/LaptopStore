    public void TimTaiKhoan(NhanVien NV){
        List<TaiKhoan> list_TaiKhoan = new TaiKhoan_BUS().getListTaiKhoan();
        for(TaiKhoan tk : list_TaiKhoan){
            if (tk.getTenDN().equals(NV.getMaNV())){
                TK = tk;
                lblName.setText(NV.getTenNV());
                lblChucVu.setText(TK.getPhanQuyen());
                return;
            }
        }
    }
    
    
    public void ShowTable1(){
        model1.setRowCount(0);
        list1 = new Laptop_BUS().getAllLaptop();
        for (Laptop s : list1) {
            if(s.getTrangThai().equals("0")){
                continue;
            }
            model1.addRow(new Object[]{
                s.getID(), s.getTen(), s.getSoLuongTonKho(), s.getGia(), s.getCPU(), s.getRAM(), s.getGPU(),
            });
        }
    }
    public void ShowTable2(){
        model2.setRowCount(0);
        list2 = new ChiTietLaptop_BUS().getListChiTietLaptop();
        for (ChiTietLaptop s : list2) {
            if(s.getTrangThai().equals("0")){
                continue;
            }
            model2.addRow(new Object[]{
                s.getIDRieng(), s.getNgayNhap(), s.getMauLapTop()
            });
        }
    }
    
